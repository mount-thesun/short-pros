    private void buttonSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSendActionPerformed
        int std = Integer.parseInt( comboBoxClass.getSelectedItem().toString() );
        String info = messageTextArea.getText();
        if(info.isEmpty())
            JOptionPane.showMessageDialog(this,"We cannot find any message to be sent!","Message",JOptionPane.UNDEFINED_CONDITION);
        else if(data.message(organisation, username, info, std))
            labelDisplay.setText("Message has been sent.");
        else
            labelDisplay.setText("Oops! Sorry, your message could not be sent.");
        labelDisplay.setVisible(true);
    }//GEN-LAST:event_buttonSendActionPerformed

    private void buttonFeedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFeedActionPerformed
        String feedback = feedbackTextArea.getText();
        if(feedback.isEmpty())
            JOptionPane.showMessageDialog(this,"We cannot find any message to be sent!","Message",JOptionPane.UNDEFINED_CONDITION);
        else if( data.feed(username, feedback) )
            labelFeedbackStatus.setText("Your feedback has been sent!");
        else
            labelFeedbackStatus.setText("Oops! Sorry, your message could not be sent.");
        labelFeedbackStatus.setVisible(true);
    }//GEN-LAST:event_buttonFeedActionPerformed

