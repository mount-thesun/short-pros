    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        getDetailsForModify();
        if(student.name.isEmpty())
            User.interrupt("Enter the student's name.");
        else if( !student.phone.isEmpty() && new User().isValidPhone(student.phone) == false)
            User.interrupt( "Invalid mobile number");
        else
            data.addStudent(student);
        viewStudents(); // getting table
    }//GEN-LAST:event_buttonAddActionPerformed

