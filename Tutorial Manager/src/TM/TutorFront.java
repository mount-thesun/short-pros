private void getDetailsForModify() {
    student.ID = Integer.parseInt(txtID.getText());
    student.name = txtName.getText();
    student.phone = txtPhone.getText();
    student.standard = (int) spinnerClass.getValue();
    student.date = (Date) spinnerDate.getValue();
    student.fee = (int) spinnerFee.getValue();
}

private void getDetailsForAttend() {
    student.ID = Integer.parseInt(textID.getText());
    student.standard = (int) spinnerStandard.getValue();
    student.date = (Date) spinnerToday.getValue();
    student.dateLong = student.date.getTime();  // getTime() returns a long which is used by sql.Date
}

private void getDetailsForPay() {
    student.ID = Integer.parseInt(textClient.getText());
    student.standard = (int) spinnerClassPay.getValue();
    String month = spinnerDue.getValue().toString();
    String year = spinnerYear.getValue().toString();
    student.month = month+","+year;
}

