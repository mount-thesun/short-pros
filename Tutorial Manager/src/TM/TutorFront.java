    private void toggleButtonPresentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_toggleButtonPresentActionPerformed
        if(textID.getText().isEmpty())
            new NoIdException();
        else {
            if(toggleButtonPresent.isSelected()) {
                toggleButtonPresent.setText("Absent");
                student.present=false;
            }
            else {
                toggleButtonPresent.setText("Present");
                student.present=true;
            }
            getDetailsForAttend();
            data.updatePresence(student);
            buttonViewActionPerformed(evt); // getting table
        }
    }//GEN-LAST:event_toggleButtonPresentActionPerformed

